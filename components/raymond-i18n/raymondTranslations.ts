export function getTranslation(key: TranslationKey, lang: SupportedLang): string {
  return translations[lang][key] ?? translations.en[key] ?? key;
}

export function t(key: TranslationKey, lang: SupportedLang, params?: Record<string, string | number>): string {
  let text = getTranslation(key, lang);
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

